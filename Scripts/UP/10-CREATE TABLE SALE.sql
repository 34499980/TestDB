
CREATE TABLE [SALE](
	[ID] [int] IDENTITY(1,1) NOT NULL,
	[DateProces] [datetime] NOT NULL,
	[IdUser] [int] NOT NULL,
	[Amount] [float] NOT NULL,
	[Refer] [nvarchar](max) NOT NULL,
	[IdSUcursal] [int] NOT NULL,
	[IdState] [int] NOT NULL,
